char	*ft_strupcase(char *str)
{
	char *start;

	start = str;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = 'A' + (*str - 'a');
		}
		str++;
	}
	return (start);
}
