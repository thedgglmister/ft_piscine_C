int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (n == 0)
	{
		return (0);
	}
	while (n > 1 && *s1 == *s2 && !(*s1 == '\0' && *s2 == '\0'))
	{
		s1++;
		s2++;
		n--;
	}
	if (*s1 == *s2)
	{
		return (0);
	}
	return (*s1 - *s2);
}
