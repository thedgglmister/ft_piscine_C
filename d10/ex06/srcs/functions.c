#include "doop.h"

long int	add(long int n, long int m)
{
	return (n + m);
}

long int	subtract(long int n, long int m)
{
	return (n - m);
}

long int	multiply(long int n, long int m)
{
	return (n * m);
}

long int	divide(long int n, long int m)
{
	return (n / m);
}

long int	mod(long int n, long int m)
{
	return (n % m);
}
